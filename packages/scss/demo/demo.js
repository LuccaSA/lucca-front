/* ANIMATIONS */
function reloadAnimation(elem) {
	var card = elem.getElementsByClassName('card')[0];
	var animationClass = card.classList[1];
	card.classList.remove(animationClass);
	setTimeout(function(){ card.classList.add(animationClass)},0);
}

